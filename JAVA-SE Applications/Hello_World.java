
public class Hello_World 
{
	public static void main(String[] args) 
	{
		System.out.println("Hello World");
	}
	
	static {
		System.out.println("static Block 1");
	}
	static {
		System.out.println("static Block 2");
	}
}



/* public - access specifier
	class - keyword
	class name is Hello_world
	static - calling by class name so static
	void - return type is there is no returning of data so void
	main - function name
	String[] args - arguments as in array of String
	System - System class
	out - object
	println() - function 
	"" - write yout message inside " "

*/
